    void mostrarEventos(){
        if (Diario.getInstance().EventosPendientes()){
            //Declaracion de varibles
            String eventos = "";
            Boolean continuar = true;
            
            //Cojo los eventos
            while(continuar){
                eventos += ("[DIARIO] " + Diario.getInstance().LeerEvento() + "\n");
                if (!Diario.getInstance().EventosPendientes())
                    continuar = false;   
            }
            
            //Muestro los eventos y actualizo la ventana
            jTextAreaEventos.setText(eventos);
            setVisible(true);
            repaint();
            revalidate();                             
        }
    }
    
